    cout <<"Variaveis do tipo 'short' ocupa " <<sizeof(short) <<" byte(s)" <<endl;
    cout <<"Variaveis do tipo 'unsigned short' ocupa " <<sizeof(unsigned short) <<" byte(s)" <<endl;
    cout <<"Variaveis do tipo 'unsigned int' ocupa " <<sizeof(unsigned int) <<" byte(s)" <<endl;
